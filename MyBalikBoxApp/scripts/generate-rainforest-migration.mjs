import { readFileSync, writeFileSync } from 'fs';
import { join } from 'path';

const API_KEY = process.env.RAINFOREST_API_KEY;
if (!API_KEY) {
  throw new Error('Missing RAINFOREST_API_KEY');
}

const categoriesPath = join(process.cwd(), 'scripts', 'data', 'rainforest-bestseller-categories.json');
const categories = JSON.parse(readFileSync(categoriesPath, 'utf8'));
const outPath = join(
  process.cwd(),
  'supabase',
  'migrations',
  '20260414190000_replace_recommended_items_from_rainforest.sql',
);

const RF = 'https://api.rainforestapi.com/request';
const TOP_N = 10;

const sqlEscape = (value) => String(value).replaceAll("'", "''");
const maybeSql = (value) => (value == null || value === '' ? 'NULL' : `'${sqlEscape(value)}'`);
const slugify = (value) =>
  String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 180);

const cache = new Map();
const rows = [];
let sortOrder = 0;

for (let categoryIndex = 0; categoryIndex < categories.length; categoryIndex += 1) {
  const { name, url } = categories[categoryIndex];
  let bestsellers = cache.get(url);
  if (!bestsellers) {
    const params = new URLSearchParams({
      api_key: API_KEY,
      type: 'bestsellers',
      url,
    });
    const response = await fetch(`${RF}?${params.toString()}`);
    const json = await response.json();
    if (!json?.request_info?.success) {
      throw new Error(`Rainforest failed for "${name}": ${JSON.stringify(json?.request_info ?? json)}`);
    }
    bestsellers = Array.isArray(json.bestsellers) ? json.bestsellers.slice(0, TOP_N) : [];
    cache.set(url, bestsellers);
  }

  for (let rank = 0; rank < bestsellers.length; rank += 1) {
    const item = bestsellers[rank];
    const title = item?.title?.trim();
    if (!title) continue;
    const asin = item?.asin?.trim() || null;
    const priceValue =
      typeof item?.price?.value === 'number'
        ? item.price.value
        : Number.isFinite(Number(item?.price?.value))
          ? Number(item.price.value)
          : 0;
    const rowSlug = asin
      ? `rf-${slugify(asin)}-c${categoryIndex + 1}-r${rank + 1}`
      : `rf-${slugify(title)}-c${categoryIndex + 1}-r${rank + 1}`;

    rows.push({
      slug: rowSlug,
      name: title,
      category: name,
      reference_price: priceValue,
      image_url: item?.image || null,
      amazon_url: item?.link || null,
      asin,
      sort_order: sortOrder,
      is_active: true,
    });
    sortOrder += 1;
  }
}

const valuesSql = rows
  .map(
    (row) =>
      `('${sqlEscape(row.slug)}','${sqlEscape(row.name)}','${sqlEscape(row.category)}',${Number.isFinite(row.reference_price) ? row.reference_price : 0},${maybeSql(row.image_url)},${maybeSql(row.amazon_url)},${maybeSql(row.asin)},${row.sort_order},true)`,
  )
  .join(',\n  ');

const sql = `-- Auto-generated once from Rainforest API bestsellers.
-- Generated by scripts/generate-rainforest-migration.mjs
begin;

delete from public.recommended_items;

insert into public.recommended_items
  (slug, name, category, reference_price, image_url, amazon_url, asin, sort_order, is_active)
values
  ${valuesSql};

commit;
`;

writeFileSync(outPath, sql, 'utf8');
console.log(`Wrote migration: ${outPath}`);
console.log(`Rows: ${rows.length}`);
